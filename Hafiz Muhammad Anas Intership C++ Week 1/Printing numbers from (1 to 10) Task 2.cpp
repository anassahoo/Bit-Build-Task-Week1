#include<iostream>
using namespace std;
int main()
{
    //Declaring variables
    int num;
    cout<<"Printing the number from 1 to 10!\n";
    //Printing the num from 1 to 10 using for loop
    for(num=1;num<=10;num++)
    {
        cout<<num<<"\n";
    }
    //Ending the program
    return 0;
}